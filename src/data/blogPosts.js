export function getBlogPostBySlug(slug) {
  return BLOG_POSTS.find((post) => post.slug === slug);
}

export function getLocalizedBlogPost(post, lang) {
  const translation = post.translations[lang] || post.translations.pt || post.translations.es;

  return {
    ...post,
    ...translation,
    category: post.category[lang] || post.category.pt,
    imageAlt: post.imageAlt[lang] || post.imageAlt.pt,
    readTime: post.readTime[lang] || post.readTime.pt,
  };
}

export function formatBlogDate(date, lang) {
  const localeByLang = {
    pt: "pt-BR",
    es: "es-AR",
    en: "en-US",
  };

  return new Intl.DateTimeFormat(localeByLang[lang] || localeByLang.pt, {
    day: "numeric",
    month: "long",
    year: "numeric",
  }).format(new Date(`${date}T00:00:00`));
}
